package org.fh.general.ecom.common.base;

import lombok.Data;

@Data
public class MessageVO implements Entity {
    /** 返回码 */
    private String code;

    /** 返回信息 */
    private String msg;

    public  MessageVO(){}

    public MessageVO(String code, String msg) {
        this.code = code;
        this.msg = msg;
    }
}
